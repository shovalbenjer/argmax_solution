def load_gold_label_personas(eval_data_dir: Path) -> pl.DataFrame:
    """
    Loads and concatenates all gold label persona tables for evaluation.
    Uses: borderline_keto.csv, strict_keto.csv, strict_vegan.csv, ground_truth_sample.csv
    """
    persona_files = [
        eval_data_dir / "borderline_keto.csv",
        eval_data_dir / "strict_keto.csv",
        eval_data_dir / "strict_vegan.csv",
        eval_data_dir / "ground_truth_sample.csv"
    ]
    
    # Define the final target schema with explicit types
    final_expected_schema = {
        'recipe_id': pl.Utf8,
        'persona': pl.Utf8,
        'reasoning': pl.Utf8,
        'is_vegan': pl.Boolean,
        'is_keto': pl.Boolean,
        'confidence': pl.Float64, 
        'title': pl.Utf8,
        'ingredients': pl.Utf8,
        'description': pl.Utf8,
        'instructions': pl.Utf8,
        'photo_url': pl.Utf8,
    }

    dfs = []
    for f in persona_files:
        if not f.exists():
            logger.error(f"Gold label persona file not found: {f}")
            continue
        try:
            # Read CSV without schema_overrides initially to infer native types
            df = pl.read_csv(f)
            
            # Rename columns if necessary
            if "vegan" in df.columns:
                df = df.rename({"vegan": "is_vegan"})
            if "keto" in df.columns:
                df = df.rename({"keto": "is_keto"})
            
            # Add missing columns with correct types
            for col, dtype in final_expected_schema.items():
                if col not in df.columns:
                    if dtype == pl.Utf8:
                        # For string columns, use empty string to ensure Utf8 type
                        df = df.with_columns(pl.lit("").cast(dtype).alias(col))
                    else:
                        # For other types, None is acceptable
                        df = df.with_columns(pl.lit(None).cast(dtype).alias(col))
            
            # Ensure all DataFrames have the same column order and exact set of columns
            df = df.select(list(final_expected_schema.keys()))
            
            dfs.append(df)
            
        except Exception as e:
            logger.error(f"Failed to load and standardize {f}: {e}")
    
    if not dfs:
        logger.error("No gold label persona tables could be loaded. Exiting.")
        sys.exit(1)
    
    # Concatenate now that all schemas are truly identical
    df_all = pl.concat(dfs)
    logger.info(f"Loaded {len(df_all)} total records from gold label persona tables.")
    
    # Add ingredients_list column using polars with_columns and map_elements
    def safe_eval_ingredients(val):
        if isinstance(val, str) and val.startswith('['):
            try:
                return ast.literal_eval(val)
            except (ValueError, SyntaxError):
                return []
        return val if isinstance(val, list) else []
    
    df_all = df_all.with_columns(
        pl.col("ingredients").map_elements(safe_eval_ingredients, return_dtype=pl.List(pl.Utf8)).alias("ingredients_list")
    )
    return df_all

async def run_classification_for_evaluation(df: pl.DataFrame) -> pl.DataFrame:
    """
    Runs the Qwen-based context-aware classification for evaluation.
    
    This function processes each recipe in the ground truth dataset using
    the ContextAwareDietClassifier. It performs asynchronous classification
    to improve performance and handles errors gracefully.
    
    Args:
        df (pl.DataFrame): Ground truth dataframe containing recipes to classify.
            Must have columns: recipe_id, title, ingredients, ingredients_list,
            is_keto, is_vegan
            
    Returns:
        pl.DataFrame: Evaluation results with the following columns:
            - recipe_id: Original recipe identifier
            - title: Recipe title
            - ingredients: Raw ingredient string
            - true_is_keto: Ground truth keto classification
            - true_is_vegan: Ground truth vegan classification
            - pred_is_keto: Qwen classifier's keto prediction
            - pred_is_vegan: Qwen classifier's vegan prediction
            - reasoning: Classification reasoning provided by Qwen
            
    Raises:
        Exception: If classifier initialization fails
        
    Example:
        >>> results_df = await run_classification_for_evaluation(ground_truth_df)
        >>> print(f"Classified {len(results_df)} recipes")
    """
    classifier = ContextAwareDietClassifier()
    predictions = []
    
    logger.info("Starting Qwen-based context-aware classification for evaluation...")
    
    # Convert to pandas for easier row iteration, then back to polars
    df_pandas = df.to_pandas()
    
    for index, row in df_pandas.iterrows():
        recipe_id = row['recipe_id']
        ingredients = row['ingredients_list']
        title = row['title']
        
        try:
            # Get predictions from our Qwen-based classifier
            result = await classifier.classify_recipe(ingredients)
            
            predictions.append({
                'recipe_id': recipe_id,
                'title': title,
                'ingredients': row['ingredients'], # Keep raw for context
                'true_is_keto': row['is_keto'],
                'true_is_vegan': row['is_vegan'],
                'pred_is_keto': result.get('recipe_is_keto', False),
                'pred_is_vegan': result.get('recipe_is_vegan', False),
                'reasoning': result.get('reasoning', 'No reasoning provided.')
            })
        except Exception as e:
            logger.error(f"Error classifying recipe {recipe_id}: {e}. Defaulting to False predictions.")
            predictions.append({
                'recipe_id': recipe_id,
                'title': title,
                'ingredients': row['ingredients'],
                'true_is_keto': row['is_keto'],
                'true_is_vegan': row['is_vegan'],
                'pred_is_keto': False,
                'pred_is_vegan': False,
                'reasoning': f"Classification failed due to error: {e}"
            })
            
    return pl.DataFrame(predictions)

def calculate_and_report_metrics(eval_df: pl.DataFrame):
    """
    Calculates and prints comprehensive classification metrics for evaluation.
    
    This function computes detailed performance metrics for both keto and vegan
    classifications, including accuracy, precision, recall, F1-score, and
    confusion matrix statistics. It also saves detailed predictions for
    further analysis.
    
    Args:
        eval_df (pl.DataFrame): Evaluation dataframe containing true and predicted
            classifications. Must have columns: true_is_keto, true_is_vegan,
            pred_is_keto, pred_is_vegan
            
    Returns:
        None: Prints metrics to console and saves results to file
        
    Example:
        >>> calculate_and_report_metrics(evaluation_results_df)
        >>> # Prints detailed classification reports and saves results
    """
    # Convert to pandas for sklearn compatibility
    eval_pandas = eval_df.to_pandas()
    
    print("\n" + "="*20 + " Keto Classification Evaluation (Qwen vs Gemini) " + "="*20)
    print(classification_report(
        eval_pandas['true_is_keto'], eval_pandas['pred_is_keto'], 
        target_names=['Not Keto', 'Keto'], zero_division=0
    ))
    
    print("\n" + "="*20 + " Vegan Classification Evaluation (Qwen vs Gemini) " + "="*20)
    print(classification_report(
        eval_pandas['true_is_vegan'], eval_pandas['pred_is_vegan'], 
        target_names=['Not Vegan', 'Vegan'], zero_division=0
    ))

    # Save detailed predictions for further analysis
    output_dir = Path("nb/src/data/evaluation_results")
    output_dir.mkdir(parents=True, exist_ok=True)
    eval_df.write_csv(output_dir / "predictions.csv")
    logger.success(f"Detailed predictions saved to {output_dir / 'predictions.csv'}")

def main():
    """
    Main execution function for the final evaluation pipeline.
    
    This function orchestrates the complete evaluation process:
    1. Loads Gemini-generated ground truth data
    2. Runs Qwen-based context-aware classification
    3. Calculates and reports performance metrics
    4. Saves detailed results for analysis
    
    The function provides comprehensive logging and error handling
    throughout the evaluation process.
    
    Returns:
        int: Exit code (0 for success, -1 for failure)
        
    Raises:
        SystemExit: If critical errors occur during evaluation
        
    Example:
        >>> exit_code = main()
        >>> if exit_code == 0:
        >>>     print("Evaluation completed successfully")
    """
    logger.info("Starting Final Evaluation of Qwen Classifier against Gold Label Personas...")
    eval_data_dir = Path(app_config.PROJECT_ROOT) / "eval_data"
    ground_truth_df = load_gold_label_personas(eval_data_dir)
    if ground_truth_df.is_empty():
        logger.error("No ground truth data available for evaluation. Exiting.")
        return -1

    # 2. Run Qwen-based context-aware classification
    start_time = time.time()
    try:
        evaluation_predictions_df = asyncio.run(run_classification_for_evaluation(ground_truth_df))
    except RuntimeError as e:
        if "asyncio.run()" in str(e) and "running event loop" in str(e):
            import nest_asyncio
            nest_asyncio.apply()
            loop = asyncio.get_event_loop()
            evaluation_predictions_df = loop.run_until_complete(run_classification_for_evaluation(ground_truth_df))
        else:
            raise
    end_time = time.time()

    logger.info("--- Evaluation Results ---")
    calculate_and_report_metrics(evaluation_predictions_df)
    
    total_time = end_time - start_time
    avg_time = total_time / len(evaluation_predictions_df) if len(evaluation_predictions_df) > 0 else 0
    logger.success(f"Evaluation finished in {total_time:.2f}s ({avg_time * 1000:.2f} ms/recipe).")
    
    return 0

if __name__ == "__main__":
    sys.exit(main()) 
