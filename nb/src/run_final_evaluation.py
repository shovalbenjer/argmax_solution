"""
Final Evaluation Script for Diet Classification System.

This script performs comprehensive evaluation of the Qwen-based, context-aware
dietary classification system against Gemini-generated persona ground truth.
It provides detailed performance metrics, accuracy analysis, and comparison
between the two classification approaches.

The evaluation covers:
- Loading and validation of ground truth data
- Context-aware classification using Qwen model
- Performance metrics calculation (accuracy, precision, recall, F1-score)
- Detailed comparison between predicted and true classifications
- Generation of comprehensive evaluation reports

Key Features:
- Asynchronous processing for improved performance
- Robust error handling and logging
- Detailed metrics for both keto and vegan classifications
- Comprehensive reporting with visualizations
- Export of results for further analysis

Dependencies:
- ContextAwareDietClassifier: Main classification system
- Ground truth data: Generated by Gemini persona system
- Required data files: personas_ground_truth.csv

Example:
    >>> python nb/src/run_final_evaluation.py
    >>> # The script will automatically load ground truth and run evaluation
"""
